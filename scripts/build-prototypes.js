#!/usr/bin/env node
'use strict';

const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const PROJECT = path.join(ROOT, 'project');
const UPLOADS = path.join(PROJECT, 'uploads');
const OUT_DIR = path.join(ROOT, 'src', 'prototypes');

const TARGETS = [
  {
    html: 'Promo Codes Prototype.html',
    out: 'promoCodes.html.js',
  },
  {
    html: 'Product Detail Prototype.html',
    out: 'productDetail.html.js',
  },
];

const SCRIPT_SRC_RE =
  /<script\s+type="text\/babel"\s+src="([^"]+)"\s*><\/script>/g;
const UPLOADS_URL_RE = /uploads\/([A-Za-z0-9._-]+\.(?:png|jpg|jpeg|gif|webp))/g;

function extToMime(ext) {
  const e = ext.toLowerCase();
  if (e === 'jpg' || e === 'jpeg') return 'image/jpeg';
  if (e === 'png') return 'image/png';
  if (e === 'gif') return 'image/gif';
  if (e === 'webp') return 'image/webp';
  return 'application/octet-stream';
}

const dataUriCache = new Map();
function uploadAsDataUri(filename) {
  if (dataUriCache.has(filename)) return dataUriCache.get(filename);
  const filePath = path.join(UPLOADS, filename);
  if (!fs.existsSync(filePath)) {
    throw new Error(`Referenced upload not found: ${filePath}`);
  }
  const ext = filename.split('.').pop();
  const b64 = fs.readFileSync(filePath).toString('base64');
  const uri = `data:${extToMime(ext)};base64,${b64}`;
  dataUriCache.set(filename, uri);
  return uri;
}

function inlineUploads(source) {
  return source.replace(UPLOADS_URL_RE, (_match, filename) =>
    uploadAsDataUri(filename),
  );
}

function inlineScripts(html) {
  return html.replace(SCRIPT_SRC_RE, (_match, src) => {
    const jsxPath = path.join(PROJECT, src);
    if (!fs.existsSync(jsxPath)) {
      throw new Error(`Referenced script not found: ${jsxPath}`);
    }
    const raw = fs.readFileSync(jsxPath, 'utf8');
    const inlined = inlineUploads(raw);
    // Inline scripts must not contain a literal "</script>" sequence.
    const safe = inlined.replace(/<\/script>/gi, '<\\/script>');
    return `<script type="text/babel" data-presets="react">\n${safe}\n</script>`;
  });
}

function toTemplateLiteral(str) {
  return str
    .replace(/\\/g, '\\\\')
    .replace(/`/g, '\\`')
    .replace(/\$\{/g, '\\${');
}

function buildOne(target) {
  const htmlPath = path.join(PROJECT, target.html);
  if (!fs.existsSync(htmlPath)) {
    throw new Error(`HTML template not found: ${htmlPath}`);
  }
  const html = fs.readFileSync(htmlPath, 'utf8');
  const withScripts = inlineScripts(html);
  const final = inlineUploads(withScripts);

  if (SCRIPT_SRC_RE.test(final)) {
    SCRIPT_SRC_RE.lastIndex = 0;
    throw new Error(`Unresolved <script src> remains in ${target.html}`);
  }
  if (UPLOADS_URL_RE.test(final)) {
    UPLOADS_URL_RE.lastIndex = 0;
    throw new Error(`Unresolved uploads/ reference remains in ${target.html}`);
  }

  const body = `// AUTO-GENERATED by scripts/build-prototypes.js — do not edit.\n// Source: project/${target.html}\nexport default \`${toTemplateLiteral(final)}\`;\n`;
  const outPath = path.join(OUT_DIR, target.out);
  fs.writeFileSync(outPath, body, 'utf8');
  return { outPath, size: Buffer.byteLength(final, 'utf8') };
}

function main() {
  fs.mkdirSync(OUT_DIR, { recursive: true });
  const results = TARGETS.map((t) => ({ target: t, ...buildOne(t) }));
  for (const r of results) {
    const kb = (r.size / 1024).toFixed(1);
    console.log(`  built ${path.relative(ROOT, r.outPath)} (${kb} KB inlined)`);
  }
}

main();
